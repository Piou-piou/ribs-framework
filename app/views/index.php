			<div class="large-6 small-12 columns">
				<h2>Sur quels sites ?</h2>
				<p>Sur un site proposé par vos soins. Un repérage en amont sera nécessaire afin de vérifier la possibilité d’installer
					nos ateliers dans ces arbres. Nous réaliserons également un diagnostic mécanique, physiologique, et pathologique des
					arbres afin d’assurer votre sécurité.
				</p>
				<p>Sur nos sites conventionnés, plus de 8 sites sont répartis dans toute la Franche-Comté.</p>

				<p>Dans tous les cas, à l’issue de l'activité tout le matériel est retiré.</p>
			</div>
			<div class="large-6 small-12 columns">
				<h2>Pour quel public ?</h2>
				<p>Nous prenons en charge toute personne de plus de 7 ans. Le nombre de participants est limité à 8 par encadrant.
					Nos ateliers peuvent être adaptés aux personnes en situation de handicap. Chaque installation est conçue selon les
					capacités des personnes et les objectifs de l’intervention.
				</p>
			</div>
