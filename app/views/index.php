			<h2>L'activité de grimpe d'arbre</h2>

			<p class="italic">Comment se rendre au plus près de la cime des arbres en toute sécurité ?<br>
				Avec un professionnel «Éducateur grimpe d’arbre».
			</p>

			<p><strong>La grimpe d'arbre est une activité de corde sportive au cœur de la nature.</strong><br>
				Elle est accessible à tous à partir de 7 ans, sans limite d'âge ou de handicap.<br>
				Nos techniques de corde sont faites pour s’adapter aux arbres mais aussi au public que nous emmenons dans cette aventure arboricole.
			</p>

			<p class="green">Pour nous l’arbre est un être vivant fascinant, et nous souhaitons vous le faire découvrir.</p>

			<p>Par petit groupe de 8 maximum par encadrant, vous serez accompagnés dans ce voyage avec vos envies, vos peurs, votre curiosité,<br>
				par un professionnel diplômé du Certificat de Qualification Professionnel « Éducateur grimpe d'arbre ».<br>
				<strong>La grimpe d'arbre s'adaptera à vos envies :</strong> détente, pause pour soi, dépassement de soi, apprendre ensemble…
			</p>

			<p>Nous utilisons du matériel certifié pour arboriste-grimpeur et <strong>vérifié pour votre sécurité. Il a été conçu pour ne pas
					dégrader les arbres.</strong>
			</p>

			<p class="green">La où il y a des arbres, il y a Arborifolia, en famille, à l’école, en vacances, au travail. Nous sommes,
				comme nos techniques, prêts à nous adapter.
			</p>

			<h3>À noter</h3>
			<p class="italic">La grimpe d’arbre a été créée à la fin des années 1980 par des arboristes-grimpeurs et des travailleurs
				sociaux ardéchois.
			</p>

			<h3>Secteur géographique</h3>
			<p><strong>La grimpe d'arbre est une activité nomade</strong>. Nous sommes implantés au cœur du département du Doubs en Franche-Comté, et nous nous déplaçons dans toute la région et les régions limitrophes.</p>
		</div>
